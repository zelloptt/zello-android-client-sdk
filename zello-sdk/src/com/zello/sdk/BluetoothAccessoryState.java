package com.zello.sdk;

/**
 * The <code>BluetoothAccessoryState</code> enum represents states of Bluetooth accessories.
 */
public enum BluetoothAccessoryState {
	/**
	 * A device has connected.
	 */
	CONNECTED,
	/**
	 * A device has disconnected.
	 */
	DISCONNECTED,
	/**
	 * An error occurred.
	 */
	ERROR
}
